for i in range(0,10,1): # step demonstration
    print(i)


for i in range(0,10,2): # runs with multiple of 2
    print(i)


for i in range(10,0,-1): # -1 makes the code run downwards
    print(i)

for i in range(10,0,-2): # -2 makes the code run downwards with multiple of 2
    print(i)

for i in range(10,0,1): # 1 makes the code run upwards but as the starting is 10 and 0 is less than 10, no output is given
    print(i)